// function to add
function add(a,b){
    return a+b;
}
// function to subtract
function sub(a,b){
    return a-b;
}
// function to multiply
function mul(a,b){
    return a*b;
}
// function to divide
function divide(a,b){
    return a/b;
}
// function to choose two operators and operating on them
function calculator(a,b,operator){
    return operator(a,b);
}
